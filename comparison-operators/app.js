// COMPARISON OPERATORS

/*
    != : Not equal to
    >  : Greater than
    <  : Less than
    >= : Greater than or equal to
    <= : Less than or equal to
*/

console.log(5 == "5"); // true
console.log(5 === "5"); // false
console.log(5 != "5"); // false
console.log(5 !== "5"); // true
console.log(7 > 3); // true
console.log(4 <= 4); // true
